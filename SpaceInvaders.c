//move player ship in response to latest ADC reading for slide potentiometer
void Move_ply(int i, unsigned long adc_in){ 
  //you have 4096 possible ADC vales but only 84 pixels
	unsigned long adc_max =4095;
	unsigned long res = 100*adc_in/adc_max;
  res = (res+5)/10;//equivalent of adding .5 so when it is rounded down to 
	//nearest integer during division a value of 99.6 will go to 100 (100.1)
	//while a value of 99.4 will go to 99 (as 99.9 is truncated to 99)  
	
	//84 is the max pixel value on the x-axis
	res =res*(84-PLAYERW)/10;//divison by ten is to offset the multiplication above to preserve mantissa
//e.g. if the slide pot is in the middle the player ship should be CENTERED
//at pixel 42. Res should be .5 * 84 but you subtract the width of the ship to center it	
//you would expect it to be subtract (PLAYERW/2) but remember the player ship
// has invisible boundaries either side so it is much larger than what you
//actually see on screen
	player[0].x = res;	
}
	
