  //SYSCTL_RCGC2_R |= 0x00000020;      // 1) F clock
  //delay = SYSCTL_RCGC2_R;            // delay to allow clock to stabilize
  SYSCTL_RCGC2_R |= 0x32; // 1) B E+F
